package day15_ForLoop;

public class ForLoopsIntroNotes {

    public static void main(String[] args) {

    // For Loops Intro

    //Example using For Loops  to repeat a print statement 10 times.
        //As long as the number is equal to 1 or number is less than or equal to 10 the for loop will continue being
        // executed.
        for(int i = 1; i <= 10;) // <-- This is the condition. (Must start with initialization).
            System.out.println("Hello World"); // This loop will be printed infinite times.

        System.out.println("-------------------------------------------------------------------------");

        for(int i = 1; i <= 10; i--)
            System.out.println("Hello World");

        System.out.println("-------------------------------------------------------------------------");

        for(int i = 1; i <= 10; i++)
            System.out.println("Hello World");

        System.out.println("-------------------------------------------------------------------------");








    }





}
