package day17_WhileAndDoLoops;

public class WhileLoopIntro {

    public static void main(String[] args) {

      if(true){
          System.out.println("Wooden Spoon"); // if the condition is true, it executes the statement only 1 time
      }
        System.out.println("-----------------------------------------------------");

        // While Loop is a repeated if statement
       while(true){
           System.out.println("Wooden Spoon"); // if the condition is true, it executes the statement until it becomes
           // false

        System.out.println("-----------------------------------------------------");





       }

    }



}
