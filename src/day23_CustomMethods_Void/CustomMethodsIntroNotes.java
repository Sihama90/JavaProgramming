package day23_CustomMethods_Void;

public class CustomMethodsIntroNotes {

    public static void main(String[] args) { //Main method can be created before or after the but not inside the
                                             // main method
        System.out.println("Test Started");

        greeting();

        System.out.println("Test Completed");

    }

    public static void greeting(){
        System.out.println("Hello Cydeo!"); // This message will be printed between "Test Started" and "Test Completed"
        System.out.println("How are you today?"); // This message will be printed between "Test Started" and "Test
                                                  // Completed"



    }





}
