package day03_EscapSequences;

public class Println_VS_PrintNotes {

    public static void main(String[] args) {

        System.err.println(); // Used to print error message (red color)

        System.out.println(); // Used to print regular outputs (black color)


        // Examples of System.out.println below.

        System.out.println("Hello Cydeo");
        System.out.println("How are you all today?");

        System.out.println("__________________________");


        // Examples of System.out.print below.

        System.out.print("Hello Cydeo");
        System.out.print("How are you all today?");

        System.out.println("Java Programming");
        System.out.print("Wooden Spoon");

        System.out.println("I love Java");





    }





}
