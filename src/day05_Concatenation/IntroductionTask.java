package day05_Concatenation;

public class IntroductionTask {

    public static void main(String[] args) {

        String name = "Siham";
        int age = 31;

        // To print - Hi, My name is Siham
        System.out.println("Hi My name is " + name);

        //To print - Hi My name is Siham, and I am 31 years old.
        System.out.println("Hi My name is " + name + ", and I am " + age + " years old.");

        // Another example: - Hello Everyone, My name is Siham, and I am 31 years old.
        System.out.println("Hello Everyone, My name is " + name + ", and I am " + age + " years old.");






    }





}
